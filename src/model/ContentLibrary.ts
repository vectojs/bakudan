function shuffle<T>(arr: readonly T[]): T[] {
  const a = [...arr] as T[];
  for (let i = a.length - 1; i > 0; i--) {
    const j = (Math.random() * (i + 1)) | 0;
    const t = a[i];
    a[i] = a[j];
    a[j] = t;
  }
  return a;
}

export const ContentLibrary = {
  entries: RAW as readonly string[],

  sample(): string {
    return RAW[(Math.random() * RAW.length) | 0]!;
  },

  sampleBatch(n: number): string[] {
    return shuffle(RAW).slice(0, n);
  },
};
